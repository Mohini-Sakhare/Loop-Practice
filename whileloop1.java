// print hello world 10 times using while loop

public class whileloop1 
{
   public static void main(String args[])
   {
    int counter=0;

    //use while loop
    while(counter<10)
    {
        System.out.println("Hello world");
        counter++;
    }
    System.out.println("print hello world 10x");
   }    
}
